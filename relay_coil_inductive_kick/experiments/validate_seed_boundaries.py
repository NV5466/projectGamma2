from __future__ import annotations

"""Boundary validator for relay_coil_inductive_kick.

This file does not merge other seed generators into this seed.
It either:
  1. validates Class A inductive cases generated by this package, or
  2. calls existing external seed scripts/adapters as negative controls.

Adapters are intentionally dumb and file/CLI based so the original signatures
keep owning their own physics.
"""

import argparse
import csv
import sys
import importlib
import json
from dataclasses import asdict
from pathlib import Path
from typing import Any, Callable, Dict, Iterable, List, Optional

import numpy as np

sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

from relay_coil_inductive_kick.classifier import classify_inductive_kick, decision_to_dict
from relay_coil_inductive_kick.generator import generate_inductive_cases, metadata_to_dict


def _load_npz_cases(path: Path) -> list[dict[str, Any]]:
    """Load cases from an NPZ with arrays named time/source/victim or cases_*.

    Supported simple schema:
      time: shape [n_samples]
      source: shape [n_cases, n_samples]
      victim: shape [n_cases, n_samples]
      source_mode: optional scalar string

    This is meant for adapters/exporters from the existing seeds.
    """
    data = np.load(path, allow_pickle=True)
    if {"time", "source", "victim"}.issubset(set(data.files)):
        time = data["time"]
        source = data["source"]
        victim = data["victim"]
        source_mode = str(data["source_mode"]) if "source_mode" in data.files else "current"
        return [
            {"time_s": time, "source": source[i], "victim": victim[i], "source_mode": source_mode}
            for i in range(source.shape[0])
        ]
    raise ValueError(f"Unsupported NPZ case schema in {path}")


def _load_adapter_cases(adapter_spec: str) -> list[dict[str, Any]]:
    """Load cases through either an NPZ path or module:function adapter.

    module:function must return an iterable of dicts with:
      time_s, source, victim, optional source_mode
    """
    if adapter_spec.endswith(".npz"):
        return _load_npz_cases(Path(adapter_spec))

    if ":" not in adapter_spec:
        raise ValueError("adapter must be an .npz path or module:function")

    module_name, func_name = adapter_spec.split(":", 1)
    module = importlib.import_module(module_name)
    func = getattr(module, func_name)
    cases = list(func())
    return cases


def validate_positive_cases(n: int, source_mode: str, seed: int) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    for i, (time_s, source, victim, meta) in enumerate(
        generate_inductive_cases(n, source_mode=source_mode, seed=seed)
    ):
        decision = classify_inductive_kick(time_s, source, victim, source_mode=source_mode)
        rows.append({
            "case_id": f"inductive_{i:04d}",
            "truth": "relay_coil_inductive_kick",
            "predicted": decision.label,
            "is_positive": decision.is_relay_coil_inductive_kick,
            "confidence": decision.confidence,
            "reason": decision.reason,
            "metadata": metadata_to_dict(meta),
            "features": decision.features,
        })
    return rows


def validate_negative_adapter(name: str, adapter_spec: str, limit: Optional[int]) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    cases = _load_adapter_cases(adapter_spec)
    if limit is not None:
        cases = cases[:limit]

    for i, case in enumerate(cases):
        decision = classify_inductive_kick(
            np.asarray(case["time_s"], dtype=float),
            np.asarray(case["source"], dtype=float),
            np.asarray(case["victim"], dtype=float),
            source_mode=case.get("source_mode", "current"),
        )
        rows.append({
            "case_id": f"{name}_{i:04d}",
            "truth": name,
            "predicted": decision.label,
            "is_positive": decision.is_relay_coil_inductive_kick,
            "confidence": decision.confidence,
            "reason": decision.reason,
            "metadata": case.get("metadata", {}),
            "features": decision.features,
        })
    return rows


def summarize(rows: list[dict[str, Any]]) -> dict[str, Any]:
    by_truth: dict[str, dict[str, Any]] = {}
    for row in rows:
        truth = row["truth"]
        bucket = by_truth.setdefault(truth, {"count": 0, "positive": 0, "negative": 0})
        bucket["count"] += 1
        if row["is_positive"]:
            bucket["positive"] += 1
        else:
            bucket["negative"] += 1

    for truth, bucket in by_truth.items():
        count = max(1, bucket["count"])
        if truth == "relay_coil_inductive_kick":
            bucket["recall"] = bucket["positive"] / count
        else:
            bucket["false_positive_rate"] = bucket["positive"] / count

    return {"by_truth": by_truth}


def write_outputs(rows: list[dict[str, Any]], out_dir: Path) -> None:
    out_dir.mkdir(parents=True, exist_ok=True)
    summary = summarize(rows)
    (out_dir / "summary.json").write_text(json.dumps(summary, indent=2), encoding="utf-8")
    (out_dir / "decisions.jsonl").write_text(
        "\n".join(json.dumps(row) for row in rows) + "\n",
        encoding="utf-8",
    )

    flat_fields = [
        "case_id", "truth", "predicted", "is_positive", "confidence", "reason",
        "source_event_count", "fit_r2", "wc2_score", "lag_us",
        "fit_gain_k", "ring_frequency_hz", "ring_q_estimate",
    ]
    with (out_dir / "features.csv").open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=flat_fields)
        writer.writeheader()
        for row in rows:
            feat = row["features"]
            writer.writerow({
                "case_id": row["case_id"],
                "truth": row["truth"],
                "predicted": row["predicted"],
                "is_positive": row["is_positive"],
                "confidence": row["confidence"],
                "reason": row["reason"],
                "source_event_count": feat.get("source_event_count"),
                "fit_r2": feat.get("fit_r2"),
                "wc2_score": feat.get("wc2_score"),
                "lag_us": feat.get("lag_us"),
                "fit_gain_k": feat.get("fit_gain_k"),
                "ring_frequency_hz": feat.get("ring_frequency_hz"),
                "ring_q_estimate": feat.get("ring_q_estimate"),
            })


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--positive-n", type=int, default=250)
    parser.add_argument("--source-mode", choices=["current", "voltage"], default="current")
    parser.add_argument("--seed", type=int, default=5466)
    parser.add_argument("--out-dir", type=Path, default=Path("boundary_results"))
    parser.add_argument(
        "--negative-adapter",
        action="append",
        default=[],
        metavar="NAME=ADAPTER",
        help="Existing external seed adapter. ADAPTER is .npz or module:function. Repeatable.",
    )
    parser.add_argument("--negative-limit", type=int, default=None)
    args = parser.parse_args()

    rows = validate_positive_cases(args.positive_n, args.source_mode, args.seed)

    for item in args.negative_adapter:
        if "=" not in item:
            raise SystemExit(f"--negative-adapter must be NAME=ADAPTER, got {item!r}")
        name, spec = item.split("=", 1)
        rows.extend(validate_negative_adapter(name, spec, args.negative_limit))

    write_outputs(rows, args.out_dir)
    print(json.dumps(summarize(rows), indent=2))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
